class DataTypes {
  public static void main(String[] args) {
      int num = 10;
      boolean flag = true;
      char letter = 'A';
      float decimal = 5.5f;
      double largeDecimal = 10.123456;
      System.out.println(num);
      System.out.println(flag);
      System.out.println(letter);
      System.out.println(decimal);
      System.out.println(largeDecimal);
  }
}